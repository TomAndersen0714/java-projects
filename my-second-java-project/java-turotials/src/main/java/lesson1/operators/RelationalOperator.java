public class RelationalOperator {
    public static void main(String[] args) {
        // relational operator
        System.out.println("(1 == 2) = " + (1 == 2));
        System.out.println("(1 >= 2) = " + (1 >= 2));
        System.out.println("(1 > 2) = " + (1 > 2));

        System.out.println("(1 <= 2) = " + (1 <= 2));
        System.out.println("(1 < 2) = " + (1 < 2));
        System.out.println("(1 != 2) = " + (1 != 2));

    }
}

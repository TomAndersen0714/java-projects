package library.lang;

/**
 * @author TomAndersen
 * @see java.lang.Number
 */
public class NumberAPI {
    public static void main(String[] args) {
        Integer num2 = 12;

        // byteValue()
        System.out.println("num2.byteValue() = " + num2.byteValue());
        // shortValue()
        System.out.println("num2.shortValue() = " + num2.shortValue());
        // intValue()
        System.out.println("num2.intValue() = " + num2.intValue());
        // longValue()
        System.out.println("num2.longValue() = " + num2.longValue());
        // floatValue()
        System.out.println("num2.floatValue() = " + num2.floatValue());
        // doubleValue()
        System.out.println("num2.doubleValue() = " + num2.doubleValue());
    }
}
